import sys, os
sys.path.append(os.path.dirname(os.path.dirname(os.path.abspath(__file__))))
"""
generate_vasp_inputs.py  (QINR-QGAN version)
=============================================
Converts geometrically-valid structures generated by the QINR-QGAN into
VASP input directories ready for DFT relaxation.
Mirrors crystalGAN/generate_vasp_inputs.py exactly — same INCAR template,
same KPOINTS density, same min_dist filter, same directory layout.

Usage:
    py -3.12 generate_vasp_inputs.py
    py -3.12 generate_vasp_inputs.py --checkpoint results_crystal_qgan/checkpoint_390.pt
    py -3.12 generate_vasp_inputs.py --n_gen 500 --min_dist 1.0

Output:
    vasp_runs/
        struct_0000/  POSCAR, INCAR, KPOINTS, POTCAR_README.txt
        struct_0001/  ...
        ...

After generating inputs:
    1. Copy POTCAR for Mg, Mn, O into each struct_XXXX/ folder
       (concatenate: cat POTCAR_Mg POTCAR_Mn POTCAR_O > POTCAR)
    2. Submit each struct_XXXX/ to your VASP queue / run locally
    3. Run parse_vasp_outputs.py once all jobs complete
"""

import argparse, os, sys, pickle, glob, re
import numpy as np
import torch

SCRIPT_DIR  = os.path.dirname(os.path.abspath(__file__))
DATASET_DIR = os.path.join(SCRIPT_DIR, "datasets")

sys.path.insert(0, SCRIPT_DIR)
sys.path.insert(0, DATASET_DIR)

from models.QINR_Crystal import PQWGAN_CC_Crystal
from view_atoms_mgmno import view_atoms
from pymatgen.core import Structure, Lattice
from pymatgen.io.vasp.inputs import Poscar, Incar, Kpoints

# ── Args ──────────────────────────────────────────────────────────────────────
parser = argparse.ArgumentParser()
parser.add_argument('--checkpoint',      type=str,   default=None,
                    help='Path to checkpoint_N.pt. Default: latest in results_crystal_qgan/')
parser.add_argument('--results_dir',     type=str,   default='./results_crystal_qgan')
parser.add_argument('--n_gen',           type=int,   default=500,
                    help='Number of structures to generate before filtering')
parser.add_argument('--min_dist',        type=float, default=1.0,
                    help='Min interatomic distance filter (Angstrom) — same as classical GAN')
parser.add_argument('--out_dir',         type=str,   default='./vasp_runs/')
parser.add_argument('--dataset',         type=str,   default=None,
                    help='Pickle for label sampling. '
                         'Default: datasets/mgmno_1000.pickle')
# Hyperparameters — must match training run
parser.add_argument('--z_dim',           type=int,   default=16)
parser.add_argument('--hidden_features', type=int,   default=6)
parser.add_argument('--hidden_layers',   type=int,   default=2)
parser.add_argument('--spectrum_layer',  type=int,   default=2)
parser.add_argument('--use_noise',       type=float, default=0.0)
opt = parser.parse_args()

device = torch.device('cuda' if torch.cuda.is_available() else 'cpu')
print(f"Device     : {device}")

# ── Resolve checkpoint ────────────────────────────────────────────────────────
if opt.checkpoint:
    ckpt_path = opt.checkpoint
else:
    all_ckpts = sorted(
        glob.glob(os.path.join(opt.results_dir, "checkpoint_*.pt")),
        key=lambda p: int(re.search(r"checkpoint_(\d+)", os.path.basename(p)).group(1))
    )
    if not all_ckpts:
        raise FileNotFoundError(f"No checkpoints found in {opt.results_dir}")
    ckpt_path = all_ckpts[-1]   # latest

print(f"Checkpoint : {ckpt_path}")

# ── Build QINR-QGAN generator ─────────────────────────────────────────────────
data_dim  = 90
label_dim = 28
z_dim     = opt.z_dim

gen_input_dim    = z_dim + label_dim       # 16 + 28 = 44
critic_input_dim = data_dim + label_dim    # 90 + 28 = 118

gan = PQWGAN_CC_Crystal(
    input_dim_g     = gen_input_dim,
    output_dim      = data_dim,
    input_dim_d     = critic_input_dim,
    hidden_features = opt.hidden_features,
    hidden_layers   = opt.hidden_layers,
    spectrum_layer  = opt.spectrum_layer,
    use_noise       = opt.use_noise,
)
generator = gan.generator.to(device)

ckpt = torch.load(ckpt_path, map_location=device, weights_only=False)
generator.load_state_dict(ckpt['generator'])
generator.to(device).eval()
epoch_loaded = ckpt.get('epoch', '?')
print(f"Loaded generator (epoch {epoch_loaded})")

# ── Label sampling: draw from training distribution ───────────────────────────
_pickle_path = opt.dataset or os.path.join(DATASET_DIR, "mgmno_1000.pickle")
_real_labels = None
if os.path.isfile(_pickle_path):
    with open(_pickle_path, "rb") as f:
        _raw = pickle.load(f)
    _real_labels = np.array([x[1] for x in _raw], dtype=np.float32).reshape(-1, 28)
    print(f"Label pool : {len(_real_labels)} training structures ({_pickle_path})")
else:
    print(f"WARNING: dataset not found at {_pickle_path}; using random labels.")

def sample_labels(n):
    if _real_labels is not None:
        idx = np.random.choice(len(_real_labels), n, replace=True)
        return torch.tensor(_real_labels[idx], dtype=torch.float32, device=device)
    mg_i = np.random.randint(0, 8,  n)
    mn_i = np.random.randint(0, 8,  n)
    o_i  = np.random.randint(0, 12, n)
    lbl  = np.zeros((n, 28), dtype=np.float32)
    for j in range(n):
        lbl[j, :mg_i[j]+1]      = 1.0
        lbl[j, 8:8+mn_i[j]+1]   = 1.0
        lbl[j, 16:16+o_i[j]+1]  = 1.0
    return torch.tensor(lbl, dtype=torch.float32, device=device)

# ── Generate structures ───────────────────────────────────────────────────────
print(f"\nGenerating {opt.n_gen} structures...")
gen_list = []
with torch.no_grad():
    for start in range(0, opt.n_gen, 32):
        bs = min(32, opt.n_gen - start)
        z  = torch.randn(bs, z_dim, device=device)
        labels = sample_labels(bs)
        gen_input = torch.cat([z, labels], dim=1)   # (bs, 44)
        gen_list.append(generator(gen_input).cpu().numpy())

gen_images = np.concatenate(gen_list, axis=0).reshape(opt.n_gen, 30, 3)

# ── INCAR template (identical to classical GAN — MP-compatible DFT+U for Mn) ──
INCAR_DICT = {
    # Electronic relaxation
    'ISTART':   0,
    'ICHARG':   2,
    'ENCUT':    520,
    'PREC':     'Accurate',
    'EDIFF':    1e-5,
    'NELM':     100,
    'ISMEAR':   0,
    'SIGMA':    0.05,
    # Ionic relaxation (full cell + positions)
    'IBRION':   2,
    'ISIF':     3,
    'NSW':      300,
    'EDIFFG':   -0.02,
    'POTIM':    0.3,
    # DFT+U for Mn (Materials Project standard: U_eff = 3.9 eV)
    'LDAU':     True,
    'LDAUTYPE': 2,
    'LDAUL':    '0 2 -1',       # Mg=s, Mn=d, O=p (no U)
    'LDAUU':    '0.0 3.9 0.0',
    'LDAUJ':    '0.0 0.0 0.0',
    'LDAUPRINT': 1,
    # Output / misc
    'NCORE':    4,
    'LWAVE':    False,
    'LCHARG':   False,
    'LORBIT':   11,
}

# ── Convert valid structures to VASP inputs ───────────────────────────────────
os.makedirs(opt.out_dir, exist_ok=True)
written = 0
skipped = 0

for i, img in enumerate(gen_images):
    try:
        atoms, _ = view_atoms(img, view=False)

        # Same geometric validity filter as classical GAN
        d = atoms.get_all_distances(mic=True)
        np.fill_diagonal(d, np.inf)
        if d.min() < opt.min_dist:
            skipped += 1
            continue

        # ASE → pymatgen
        cell    = atoms.get_cell().tolist()
        pos     = atoms.get_scaled_positions().tolist()
        symbols = atoms.get_chemical_symbols()
        struct  = Structure(Lattice(cell), symbols, pos)

        struct_dir = os.path.join(opt.out_dir, f'struct_{written:04d}')
        os.makedirs(struct_dir, exist_ok=True)

        Poscar(struct).write_file(os.path.join(struct_dir, 'POSCAR'))
        Incar(INCAR_DICT).write_file(os.path.join(struct_dir, 'INCAR'))
        Kpoints.automatic_density(struct, kppa=1000).write_file(
            os.path.join(struct_dir, 'KPOINTS'))

        with open(os.path.join(struct_dir, 'POTCAR_README.txt'), 'w') as f:
            f.write(
                "Copy POTCAR for Mg, Mn, O from your VASP installation:\n"
                "  cat $VASP_PP/Mg/POTCAR $VASP_PP/Mn_pv/POTCAR $VASP_PP/O/POTCAR > POTCAR\n"
            )

        written += 1

    except Exception:
        skipped += 1

print(f"\nDone.")
print(f"  Checkpoint                       : {os.path.basename(ckpt_path)} (epoch {epoch_loaded})")
print(f"  Generated                        : {opt.n_gen} structures")
print(f"  Valid structures written (>= {opt.min_dist} A): {written}")
print(f"  Skipped (invalid / error)        : {skipped}")
print(f"  Output directory                 : {opt.out_dir}")
print(f"\nNext steps:")
print(f"  1. Add POTCAR to each struct_XXXX/  (see POTCAR_README.txt)")
print(f"  2. Run VASP in each directory")
print(f"  3. Run: py -3.12 parse_vasp_outputs.py")
